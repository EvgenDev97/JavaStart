package DataType;

public class Char {
    public static void main(String args[]) {
        char ch;
        ch = 'X';
        System.out.println("ch = " + ch);
        ch++;
        System.out.println("ch =" + ch);
        ch = 90;
        System.out.println("ch = " + 90);
        var a = "dog likes socks";
        a = "123";
        System.out.println(a);
        var str = "10";
        var i = Integer.parseInt(str.trim());
        i = 10;
        System.out.println(i);
    }

}
